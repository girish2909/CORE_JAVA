package com.pih.collection.set;

import java.util.HashSet;

public class HashSetDemo {

	public static void main(String[] args) {
		HashSet<String> hs = new HashSet<String>();
		
		hs.add("Beta");
		hs.add("Alfa");
		hs.add("Eeta");
		hs.add("Gamma");
		hs.add("Epsilen");
		hs.add("Omega");
		
		System.out.println(hs);
	}
}
